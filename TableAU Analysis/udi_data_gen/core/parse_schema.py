"""Parse UDI Model Tables.xlsx into a structured metadata.json.

The workbook is laid out as a grid of 4 column-groups per row-band:
    cols 0-2, 5-7, 10-12, 15-17  -> (Table in Model | Column Name | DataType)
Each band starts with a header row, then a data row whose first cell is the
table name, followed by additional rows listing columns until a blank row.
"""

from __future__ import annotations

import json
import re
from pathlib import Path

import pandas as pd

ROOT = Path(__file__).resolve().parents[2]
EXCEL_PATH = ROOT / "UDI Model Tables.xlsx"
OUT_PATH = ROOT / "udi_data_gen" / "metadata.json"

GROUP_OFFSETS = (0, 5, 10, 15)  # starting column of each (Table, Column, DataType) group


def _norm(v):
    if v is None:
        return None
    s = str(v).strip()
    if s == "" or s.lower() == "nan":
        return None
    return s


def parse_workbook() -> dict:
    df = pd.read_excel(EXCEL_PATH, sheet_name="Sheet1", header=None)
    n_rows = len(df)

    # Locate header rows ("Table in Model" in column 0)
    header_rows = [i for i in range(n_rows) if _norm(df.iat[i, 0]) == "Table in Model"]

    tables: dict[str, dict] = {}

    for h_idx, h_row in enumerate(header_rows):
        next_h = header_rows[h_idx + 1] if h_idx + 1 < len(header_rows) else n_rows
        # Data rows for this band: h_row+1 .. next_h-1
        for off in GROUP_OFFSETS:
            t_col, c_col, d_col = off, off + 1, off + 2
            # The first data row has the table name
            r = h_row + 1
            if r >= n_rows:
                continue
            table_name = _norm(df.iat[r, t_col])
            if not table_name:
                continue
            columns = []
            for rr in range(r, next_h):
                col_name = _norm(df.iat[rr, c_col])
                dtype = _norm(df.iat[rr, d_col])
                if col_name is None and dtype is None:
                    break
                if col_name is None:
                    continue
                columns.append({"name": col_name, "sql_type": dtype})
            if columns:
                tables[table_name] = {
                    "name": table_name,
                    "columns": columns,
                }

    return classify(tables)


# ---------- Classification & relationship inference ----------

# Columns that act as foreign keys across the model
KEY_COLS = {
    "BrandSequence",
    "SBRSequence",
    "CalendarDate",
    "CardBrandSequence",
    "StoreBrandSequence",
    "MergedBrandSequence",
    "UnitNumber",
    "SBRNumber",
}

# Tables explicitly playing a dimension role
DIM_TABLE_NAMES = {
    "Brand",
    "Unit",
    "Unit Ranking Base",  # extended unit attributes / ranking dim
}


def is_measure(col_name: str, sql_type: str | None) -> bool:
    if col_name in KEY_COLS:
        return False
    if sql_type is None:
        return False
    t = sql_type.lower()
    return t in {"int", "bigint", "float", "numeric", "decimal", "money", "bit"} and not col_name.endswith(
        ("Sequence", "Number", "Date")
    )


def infer_role(table_name: str, columns: list[dict]) -> str:
    if table_name in DIM_TABLE_NAMES:
        return "dimension"
    col_names = {c["name"] for c in columns}
    # Targets table — brand-level planning data, treat as fact (sparse measures)
    if "Target" in table_name:
        return "fact"
    # Heuristic: has at least one of the FK keys AND some measure
    if (col_names & KEY_COLS) and any(is_measure(c["name"], c["sql_type"]) for c in columns):
        return "fact"
    return "dimension"


def infer_relationships(tables: dict[str, dict]) -> list[dict]:
    """Map FK-like columns in fact tables to dimension PKs."""
    # Establish dim PKs
    dim_pks = {
        "Brand": ["BrandSequence"],
        "Unit": ["SBRSequence", "UnitNumber"],
        "Unit Ranking Base": ["SBRSequence", "BrandSequence", "CalendarDate"],
        # virtual date dim (generated by us)
        "DimDate": ["CalendarDate"],
    }

    fk_map = {
        "BrandSequence": ("Brand", "BrandSequence"),
        "CardBrandSequence": ("Brand", "BrandSequence"),
        "StoreBrandSequence": ("Brand", "BrandSequence"),
        "MergedBrandSequence": ("Brand", "BrandSequence"),
        "SBRSequence": ("Unit", "SBRSequence"),
        "UnitNumber": ("Unit", "UnitNumber"),
        "SBRNumber": ("Unit", "UnitNumber"),  # alt key
        "CalendarDate": ("DimDate", "CalendarDate"),
    }

    rels = []
    for tname, t in tables.items():
        if t["role"] != "fact":
            continue
        for col in t["columns"]:
            cn = col["name"]
            if cn in fk_map:
                ref_t, ref_c = fk_map[cn]
                rels.append(
                    {
                        "from_table": tname,
                        "from_column": cn,
                        "to_table": ref_t,
                        "to_column": ref_c,
                    }
                )
    return rels


def classify(tables: dict[str, dict]) -> dict:
    # Assign role + mark columns
    for tname, t in tables.items():
        role = infer_role(tname, t["columns"])
        t["role"] = role
        for col in t["columns"]:
            cn = col["name"]
            col["is_key"] = cn in KEY_COLS
            col["is_measure"] = role == "fact" and is_measure(cn, col["sql_type"])
            col["nullable"] = not col["is_key"]

    # Add primary key & grain hints
    pk_overrides = {
        "Brand": ["BrandSequence"],
        "Unit": ["SBRSequence"],
        "Unit Ranking Base": ["SBRSequence", "BrandSequence", "CalendarDate"],
    }
    for tname, t in tables.items():
        if tname in pk_overrides:
            t["primary_key"] = pk_overrides[tname]
        elif t["role"] == "fact":
            keys = [c["name"] for c in t["columns"] if c["is_key"]]
            t["primary_key"] = keys  # composite grain
        else:
            t["primary_key"] = []

    # Add a synthetic DimDate dimension descriptor so date FKs resolve
    if "DimDate" not in tables:
        tables["DimDate"] = {
            "name": "DimDate",
            "role": "dimension",
            "primary_key": ["CalendarDate"],
            "synthetic": True,
            "columns": [
                {"name": "CalendarDate", "sql_type": "date", "is_key": True, "is_measure": False, "nullable": False},
                {"name": "DateKey", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "Year", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "Quarter", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "Month", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "MonthName", "sql_type": "nvarchar", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "Week", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "DayOfMonth", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "DayOfWeek", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "DayName", "sql_type": "nvarchar", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "IsWeekend", "sql_type": "bit", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "IsHoliday", "sql_type": "bit", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "FiscalYear", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
                {"name": "FiscalQuarter", "sql_type": "int", "is_key": False, "is_measure": False, "nullable": False},
            ],
        }

    relationships = infer_relationships(tables)

    # Volume strategy
    volumes = {}
    for tname, t in tables.items():
        if tname == "Brand":
            volumes[tname] = 50
        elif tname == "Unit":
            volumes[tname] = 8_000
        elif tname == "DimDate":
            volumes[tname] = 365 * 10 + 3
        elif tname == "Unit Ranking Base":
            volumes[tname] = "derived"
        elif "Target" in tname:
            volumes[tname] = "monthly_per_brand"  # ~12 * years * brands
        else:
            # Facts: daily grain per active unit
            volumes[tname] = "daily_per_unit"

    return {
        "source_file": EXCEL_PATH.name,
        "tables": tables,
        "relationships": relationships,
        "volume_strategy": volumes,
        "notes": {
            "grain": "Most fact tables are at (BrandSequence, SBRSequence, CalendarDate) daily grain.",
            "Brand Targets": "Brand-level grain (BrandSequence, CalendarDate) — monthly targets.",
            "Gift Cards": "Has three Brand-role FKs (Card/Store/Merged) all referencing Brand.BrandSequence.",
            "Unit": "Acts as the Store dimension; SBRSequence is its PK and a FK target for facts.",
            "Unit Ranking Base": "Snapshot/ranking dimension used for comp-store calculations; derived from Unit + Reported Sales.",
        },
    }


def main() -> None:
    meta = parse_workbook()
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUT_PATH.write_text(json.dumps(meta, indent=2))
    # Summary print
    facts = [t for t, v in meta["tables"].items() if v["role"] == "fact"]
    dims = [t for t, v in meta["tables"].items() if v["role"] == "dimension"]
    print(f"Parsed {len(meta['tables'])} tables -> {OUT_PATH}")
    print(f"  Facts ({len(facts)}): {facts}")
    print(f"  Dims  ({len(dims)}): {dims}")
    print(f"  Relationships: {len(meta['relationships'])}")
    for t in meta["tables"].values():
        print(f"   - {t['name']:22s} role={t['role']:9s} cols={len(t['columns'])} pk={t['primary_key']}")


if __name__ == "__main__":
    main()
